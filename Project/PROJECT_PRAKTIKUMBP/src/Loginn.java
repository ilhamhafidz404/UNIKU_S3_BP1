    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String Username = user.getText();
    String Password = pass.getText();
    
    if(Username.isEmpty() || Password.isEmpty()){
        JOptionPane.showMessageDialog(this,"Data tidak boleh kosong !","Error",JOptionPane.ERROR_MESSAGE);
    } else {
         try {
        String sql = "SELECT*FROM login WHERE user = ? AND pass=? ";
         try (PreparedStatement pst = koneksi.con.prepareStatement(sql)) {
        pst.setString(1, Username);
        pst.setString(2, Password);
        
        rs = pst.executeQuery();
        if(rs.next()){
              JOptionPane.showMessageDialog(this, "Login Berhasil");
       
        }else {
            JOptionPane.showMessageDialog(this, "Login Gagal username & password salah","Eror",JOptionPane.ERROR_MESSAGE);
            user.setText("");
            pass.setText("");
        }
      
    }
    }catch (Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

